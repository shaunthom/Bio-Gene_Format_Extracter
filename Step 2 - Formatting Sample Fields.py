def process_sample_fields(format_field, sample_field):
    final_dict = {}
    list_format = format_field.split(':')
    # Splitting the format field

    for item_key, value in sample_field.items():
        value_sample_field = value.split(':')
        # Generating the list of sample field dictionary values

        output_field_dict = dict(zip(list_format, value_sample_field))
        final_dict[item_key] = output_field_dict

    return final_dict

